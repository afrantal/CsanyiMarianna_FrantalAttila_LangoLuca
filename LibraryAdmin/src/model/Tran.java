
package model;


public class Tran {
      
      private int direction;
      private int bid;
      private int mid;


      public Tran(){}

      public int getDirection() {
            return direction;
      }

      public int getBid() {
            return bid;
      }

      public int getMid() {
            return mid;
      }

      public void setDirection(int direction) {
            this.direction = direction;
      }

      public void setBid(int bid) {
            this.bid = bid;
      }

      public void setMid(int mid) {
            this.mid = mid;
      }

}
